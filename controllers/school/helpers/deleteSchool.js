import { SchoolSchema } from "../../../schemas/School/index.js";

export const deleteSchool = async (req, res) => {
  const { id } = req.params;
  try {
    const data = await SchoolSchema.findById(id);
    if (!data) {
      return res.json({ success: false, message: "Invalid Id" });
    }
    const deleteRecord = await data.delete();
    if (!deleteRecord) {
      return res.json({ success: false, message: "Unexpected Error" });
    }
    return res.json({
      success: true,
      message: "This school record has been deleted successfully",
    });
  } catch (e) {
    console.log("Error:", e.message);
    return res.json({
      success: false,
      message: "Internal Server Error",
    });
  }
};
